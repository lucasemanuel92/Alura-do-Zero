// Contador de 0 - 10
let contador = 1;

while (contador <= 10) {
    console.log(contador);
    contador++;
}

// Contador de 10 - 0
let contrario = 10;

while (contrario >= 0) {
    console.log(contrario);
    contrario--;
}

// Contagem regressiva com alguém escolhendo de onde começar
let contagemRegressiva = prompt("Insira o numero de onde começar: ");

while (contagemRegressiva >= 0) {
    console.log(contagemRegressiva);
    contagemRegressiva--;
}

